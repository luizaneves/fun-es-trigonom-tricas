from random import randint

from time import sleep
pc = randint(0,5) #Faz o computador "pensar"
print('--*--' * 16)
print('Pensarei em um número entre 0 e 5. Tente advinhar em qual número eu pensei')
print('--*--' * 16)
player = int(input('Em que número você acha que eu pensei?')) #Jogador tenta advinhar
print('COMPUTANDO A SUA RESPOSTA...')
sleep(3)

if player == pc:
    print('Parabéns, você acertou o numero que eu estava pensando')
else:
    print('Não foi dessa vez que você acertou o numéro que eu pensei, na verdade era o {}'.format(pc))

